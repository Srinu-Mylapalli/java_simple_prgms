class UnaryOp{
	public static void  main(String[] args){
		int i=50,j,k,l,m;
		j=++i;
		k=i++;
		l=--i;
		m=i++;
		i=50;
		System.out.println(" i value is: "+i);
		System.out.println(" j value is: "+j);
		System.out.println(" k value is: "+k);
		System.out.println(" l value is: "+l);
		System.out.println(" m value is: "+m);
	}
}
